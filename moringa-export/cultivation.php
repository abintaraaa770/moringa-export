        <div class="article-content">

           <h3>1) Root (Radix)</h3>

        <p>
            Moringa develops a distinctive taproot system, typically white in color. 
            The root structure plays a critical role in plant stability, drought tolerance, 
            and long-term resilience.
        </p>

        <p>
            The root bark possesses a sharp and pungent taste and aroma, with a pale-yellow 
            interior characterized by fine longitudinal and crosswise lining. In seed-grown 
            plants, the taproot enlarges similarly to a radish, forming a bulbous base 
            that supports deep soil penetration.
        </p>

        <p>
            Trees propagated from seeds develop deep and expansive taproots accompanied 
            by thick fibrous roots. Conversely, plants propagated through stem cuttings 
            typically lack taproot formation.
        </p>

        <div class="highlight-box">
            Taproot development significantly enhances drought resistance, nutrient uptake, 
            and structural stability, making seed propagation preferable for commercial cultivation.
        </div>

            <h3>2) Stem (Caulis)</h3>

            <p>
                Moringa is classified as a fast-growing woody shrub capable of reaching 
                heights between 7 – 12 meters under optimal conditions.
            </p>

            <p>
                The stem is round (teres) with a relatively rough surface texture. Growth 
                direction is erect (erectus), supporting vertical development and extensive branching.
            </p>

            <p>
                Branching follows a sympodial pattern, where the dominance of the main stem 
                may shift as lateral branches expand and compete for growth.
            </p>

           <h3>3) Leaf (Folium)</h3>

            <p>
                Moringa exhibits compound leaves arranged alternately, featuring odd-pinnate 
                leaflet structures (imparipinnatus).
            </p>

            <p>
                Leaf coloration transitions from light green in early growth stages to dark green 
                upon maturity. Leaflets are thin, pliable, and ovate in shape, typically measuring 
                between 1 – 2 cm.
            </p>

            <p>
                The venation pattern is pinnate (penninervis), resembling the structure of fish bones, 
                contributing to efficient nutrient and water transport.
            </p>

            <h3>4) Flower</h3>

            <p>
                Flowers emerge from leaf axils (axillaris) with elongated stalks. Petals 
                are whitish-cream and emit a characteristic fragrance.
            </p>

            <p>
                Inflorescences appear as pendulous panicles measuring approximately 10 – 15 cm. 
                Flowering may occur throughout the year under favorable climatic conditions.
            </p>

           <h3>5) Fruit or Pod</h3>

            <p>
                Fruiting typically begins between 12 – 18 months after planting. Pods are 
                triangularly elongated and range from 20 – 60 cm.
            </p>

            <p>
                Immature pods display green coloration, gradually turning brown upon maturity 
                and drying.
            </p>
           
           <h3>6) Seed</h3>

            <p>
                Seeds are round with a semi-permeable protective coat featuring three 
                characteristic whitish wings.
            </p>

            <p>
                A mature tree may produce between 15,000 – 25,000 seeds annually, with 
                an average seed weight of approximately 0.3 g.
            </p>
            
