                <h2>1. Basic Preparation Before Planting</h2>

                <ul class="article-list">
                    <li>Dig planting holes 20 × 20 × 20 cm at least one day before planting.</li>
                    <li>Fill holes with a mixture of soil and organic compost/manure.</li>
                    <li>Ensure the planting area is free from weeds.</li>
                    <li>Prepare either stem cuttings or healthy seedlings.</li>
                </ul>

                <div class="highlight-box">
