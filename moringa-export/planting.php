                <h2>2. Planting Methods</h2>

                <h3>Planting Stem Cuttings</h3>

                <p>
                    Stem cuttings are commonly used for rapid propagation, 
                    especially in home gardens or fencing systems.
                </p>

                <ul class="article-list">
                    <li>Best planting time: End of dry season before rainy season.</li>
                    <li>Prepare holes 10–15 cm deep.</li>
                    <li>Plant cuttings at a slanted angle.</li>
                    <li>Bury approximately one-third of the cutting length.</li>
                    <li>Cut the base diagonally to stimulate rooting.</li>
                    <li>Plant within 3 days after cutting.</li>
                </ul>

                <div class="highlight-box">
                    Cuttings grow fast but lack taproot formation, 
                    making plants less resistant to drought and wind stress.
                </div>

                <h3>Planting Seedlings (From Seeds)</h3>

                <p>
                    Seed propagation is strongly recommended for commercial cultivation.
                </p>

                <ul class="article-list">
                    <li>Select strong, healthy seedlings.</li>
                    <li>Transplant carefully to avoid root damage.</li>
                    <li>Water immediately after planting.</li>
                    <li>Apply organic mulch for moisture retention.</li>
                </ul>

                <h2>3. Direct Seeding in the Field</h2>

                <p>
                    Direct seeding offers operational efficiency and 
                    stronger early root establishment.
                </p>

                <ul class="article-list">
                    <li>Plant treated seeds 2–3 cm deep.</li>
                    <li>Maintain soil moisture during early growth.</li>
                    <li>Control weeds regularly.</li>
                    <li>Prepare backup seedlings if required.</li>
                </ul>

                <div class="highlight-box">
                    Correct planting depth, spacing, and weed control 
                    are critical success factors.
                </div>

